    public void AgrgarPelicula(){
        Date fecha = DataFecha.getDate();
        
        if(!TxtTitulo.getText().isEmpty() && !TxtDirector.getText().isEmpty() 
                && !TxtDuracion.getText().isEmpty() && fecha != null && !TxtIdioma.getText().isEmpty()
                && !TxtSinopsis.getText().isEmpty())
        {
            if (!TxtGenero.getText().isEmpty() && ComboGenero.getSelectedIndex()> 0) {
                   JOptionPane.showMessageDialog(this, 
                    "Solo puede llenar uno de los dos campos: escriba el género o selecciónelo, no ambos.");
            } else if (TxtGenero.getText().isEmpty() && ComboGenero.getSelectedIndex() <=0) {
                JOptionPane.showMessageDialog(this, 
                    "Debe escribir el género o seleccionarlo.");
            } else {
                if(!TxtGenero.getText().isEmpty()){
                    genero = TxtGenero.getText();
                }else{
                    genero = ComboGenero.getSelectedItem().toString();
                }
                
                int id = 0;
                sinopsis = TxtSinopsis.getText();
                idioma = TxtIdioma.getText();
                titulo = TxtTitulo.getText();
                duracion = TxtDuracion.getText();
                Estreno = new SimpleDateFormat("yyyy-MM-dd").format(fecha);
                director = TxtDirector.getText();
                Peliculas pelicula =new Peliculas(id,titulo,genero,duracion,Estreno,director,sinopsis,idioma);
                try {
                    Connection con = conn.Conexion();
                    CallableStatement cstmt = con.prepareCall("call AgregarPelicula(?, ?, ?, ?, ?, ?, ?)");
                    cstmt.setString(1, pelicula.getTitulo());
                    cstmt.setString(2, pelicula.getDirector());
                    cstmt.setString(3, pelicula.getSinopsis());
                    cstmt.setString(4, pelicula.getDuracion());
                    cstmt.setString(5, pelicula.getEstreno());
                    cstmt.setString(6, pelicula.getIdioma());
                    cstmt.setString(7, pelicula.getGenero());            

                    cstmt.execute();

                    JOptionPane.showMessageDialog(null, "Película agregada correctamente");

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Error: al guardar " + e.getMessage());
                }
            }
        }else
        {
            JOptionPane.showMessageDialog(rootPane, "Faltan paremetros");
        }
    }
    
    public void LlenarComboGenero(){
        try {
            Connection con = conn.Conexion();
            String sql = "call listar_generos()";
            
            CallableStatement  cstmt  = con.prepareCall(sql);
            
            ResultSet resultado = cstmt.executeQuery();
            
            ComboGenero.removeAllItems();
            ComboGenero.addItem("Seleccione...");
            
            while(resultado.next()){
                ComboGenero.addItem(resultado.getString("nombre_genero"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al cargar genero" + e);
        }
    }
    
