    public void mostrarTodosMisTickets() {
        DefaultTableModel modelo = new DefaultTableModel(
            new String[]{"Ticket", "Asiento", "Inicio", "Precio", "Pel√≠cula", "Sala"}, 0);

        String tipo = Sesion.usuarioActivo.getRol();
        int idPersona;
        
        if(Sesion.usuarioActivo.getId() == 0){
            idPersona = Sesion.usuarioActivo.getIde();
        }else{
            idPersona = Sesion.usuarioActivo.getId();
        }

        try{
            Connection con =  conn.Conexion();
            String sql = "call mis_tickets(?, ?)";
            CallableStatement cstmt = con.prepareCall(sql);
            cstmt.setString(1, tipo);
            cstmt.setInt(2, idPersona);
            ResultSet rs = cstmt.executeQuery();

            while (rs.next()) {
                modelo.addRow(new Object[]{
                    rs.getInt("ticket_id"),
                    rs.getInt("numero"),
                    rs.getTimestamp("inicio"),
                    rs.getDouble("precio_final"),
                    rs.getString("titulo"),
                    rs.getString("sala")
                });
            }

            TableReportes.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar tickets: " + e.getMessage());
        }
    }

