    public void mostrarPerfilPorId() {
        
        try{
            Connection con = conn.Conexion();
            String sql = "call perfil(?, ?)";
            CallableStatement cstmt = con.prepareCall(sql);
            cstmt.setString(1, tipo); 
            cstmt.setInt(2, idPersona);

            ResultSet rs = cstmt.executeQuery();

            if (rs.next()) {
                String nombre   = rs.getString("nombre");
                String apellido = rs.getString("apellido");
                String email    = rs.getString("email");
                String telefono = rs.getString("telefono");
                String usuario  = rs.getString("usuario");
                String rol      = rs.getString("rol");
                
                if(rol.equalsIgnoreCase("cliente")){
                    LblCargo1.setVisible(false);
                    lblCargo2.setVisible(false);
                    LblNombre.setText(nombre);
                    LblApellido.setText(apellido);
                    LblEmail.setText(email);
                    LblUsuario.setText(usuario);
                }else{
                    LblCargo1.setVisible(true);
                    lblCargo2.setVisible(true);
                    lblCargo2.setText(rol);
                    LblNombre.setText(nombre);
                    LblApellido.setText(apellido);
                    LblEmail.setText(email);
                    LblUsuario.setText(usuario);
                }               
                
            } else {
                JOptionPane.showMessageDialog(null, "No se encontr√≥ el " + tipo + " con ID " + idPersona);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }

